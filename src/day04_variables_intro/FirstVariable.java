package day04_variables_intro;

public class FirstVariable {
    public static void main(String[] args) {
        //declare variable n thant can store int(whole number)
        int n;
        //assin/put value 10 to n
        n = 10;
        //print value n variable
        System.out.println(n);
        System.out.println("n");

        int apples;
        apples = 50;
        System.out.println(apples);

        //declare variable bananas, assign 100, print value out
        int bananas;
        bananas = 100;
        System.out.println(bananas);
    }
}
